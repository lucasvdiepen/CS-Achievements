using System;
using System.Collections.Generic;
using System.Text;

namespace Les3Classes
{
    class Rekenmachine
    {
        public int Optellen(int waarde1, int waarde2)
        {
            return waarde1 + waarde2;
        }

        public float Delen(float waarde1, float waarde2)
        {
            return waarde1 / waarde2;
        }

        public int Vermenigvuldigen(int waarde1, int waarde2)
        {
            return waarde1 * waarde2;
        }
    }
}
